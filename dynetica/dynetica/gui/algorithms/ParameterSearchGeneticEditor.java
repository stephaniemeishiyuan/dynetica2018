    public void removeSelectedObjective() {
        Object selected = targetListBox.getSelectedValue();
        if (selected == null) {
            return;
        }
        String name = (String) selected;
        targetObjectives.removeElement(selected);
        for (TargetObjective to : targetList) {
            if (name.equals(to.getSubstance().getName())) {
                targetList.remove(to);
                break;
            }
        }
    }

