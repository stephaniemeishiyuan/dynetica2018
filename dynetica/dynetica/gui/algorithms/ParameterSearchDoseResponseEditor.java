    private void runButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_runButtonActionPerformed
        normalizeObjectiveWeights();
        String variableName = (String) variableSelector.getSelectedItem();
        variable = (Variable) system.getEntity(variableName);
        if (variable == null) {
            System.out.println("WHY!?!?!?");
            return;
        }
        min = Double.parseDouble(minField.getText());
        max = Double.parseDouble(maxField.getText());
        numPoints = Integer.parseInt(numPointsField.getText());
        logScale = logScaleCheckBox.isSelected();
        double[] xValues;
        if (logScale) {
            xValues = Numerics.logspace(min, max, numPoints);
        } else {
            xValues = Numerics.linspace(min, max, numPoints);
        }
        int popSize = Integer.parseInt(populationSizeField.getText());
        int generations = Integer.parseInt(numGenerationsField.getText());
        double threshold = Double.parseDouble(thresholdField.getText());
        time = Double.parseDouble(timeField.getText());
        boolean randomize = randomizeCheckBox.isSelected();
        Parameter[] parameterList = new Parameter[searchParametersListModel
                .size()];
        for (int i = 0; i < parameterList.length; i++) {
            parameterList[i] = (Parameter) system
                    .getParameter((String) searchParametersListModel.get(i));
        }

        psdr = new ParameterSearchDoseResponse(system, parameterList,
                objectivesList, variable, xValues, time, threshold,
                generations, popSize, randomize);
        psdr.start();
    }// GEN-LAST:event_runButtonActionPerformed

