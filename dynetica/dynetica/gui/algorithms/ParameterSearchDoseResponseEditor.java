    private void editObjectiveButtonActionPerformed(
            java.awt.event.ActionEvent evt) {// GEN-FIRST:event_editObjectiveButtonActionPerformed
        String id = (String) objectivesListBox.getSelectedValue();
        objectivesListModel.removeElement(id);
        for (int i = 0; i < objectivesList.size(); i++) {
            TargetDoseObjective tdo = objectivesList.get(i);
            if (tdo.toString().equalsIgnoreCase(id)) {
                JFrame jf = new JFrame("Dose Response Objective Editor");
                jf.getContentPane().add(new DoseObjectiveEditor(jf, this, tdo));
                jf.pack();
                jf.show();
                objectivesList.remove(tdo);
                break;
            }
        }

    }// GEN-LAST:event_editObjectiveButtonActionPerformed

