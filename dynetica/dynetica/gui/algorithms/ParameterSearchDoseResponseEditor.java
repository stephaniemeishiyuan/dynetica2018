    private void plotButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_plotButtonActionPerformed
        if (psdr == null) {
            return;
        }
        if (psdr.getSimulationThread() != null) {
            return;
        }
        ArrayList<TargetDoseObjective> tdoList = psdr.getObjectives();
        ArrayList<AbstractMetric> metricList = new ArrayList<AbstractMetric>();
        for (TargetDoseObjective tdo : tdoList) {
            metricList.add(tdo.getMetric());
        }
        SensitivityAnalysis sa = new SensitivityAnalysis(variable, metricList,
                system, min, max, numPoints, time, logScale);
        sa.setPlotOnComplete(true);
        sa.start();
        System.out.println("Analysis complete.");
    }// GEN-LAST:event_plotButtonActionPerformed

