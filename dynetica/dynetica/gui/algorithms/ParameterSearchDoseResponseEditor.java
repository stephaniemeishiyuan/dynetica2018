    private void setUpLists() {
        List substances = system.getSubstances();
        List parameters = system.getParameters();
        for (int i = 0; i < substances.size(); i++) {
            Substance s = (Substance) substances.get(i);
            if (s instanceof ExpressionVariable) {
                continue;
            }
            variableSelectorModel.addElement(s.getName());
        }
        for (int i = 0; i < parameters.size(); i++) {
            Parameter p = (Parameter) parameters.get(i);
            if (p.getName().equals("Time")) {
                continue;
            }
            variableSelectorModel.addElement(p.getName());
            allParametersListModel.addElement(p.getName());
        }
    }

    public void addObjective(TargetDoseObjective tdo) {
        objectivesList.add(tdo);
        objectivesListModel.addElement(tdo.toString());
    }

    public void normalizeObjectiveWeights() {
        double sum = 0;
        for (TargetDoseObjective tdo : objectivesList) {
            sum += tdo.getWeight();
        }
        if (sum == 1) {
            return;
        }
        for (TargetDoseObjective tdo : objectivesList) {
            tdo.setWeight(tdo.getWeight() / sum);
        }
        System.out.println("Normalizing objective weights...");
    }

