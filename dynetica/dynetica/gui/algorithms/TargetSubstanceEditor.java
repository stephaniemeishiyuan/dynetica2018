    public TargetSubstanceEditor(ParameterSearchMultiSubstanceEditor p,
            TargetSubstance ts) {
        psmse = p;
        system = p.system;
        initComponents();
        setUpList();
        substanceChoice.setSelectedIndex(substanceBox.getIndexOf(ts.getName()));
        targetField.setText(Double.toString(ts.getTarget()));
        weightField.setText(Double.toString(ts.getWeight()));
    }

    private void setUpList() {
        List substances = system.getSubstances();
        for (int i = 0; i < substances.size(); i++) {
            Substance s = (Substance) substances.get(i);
            substanceBox.addElement(s.getName());
        }
    }

