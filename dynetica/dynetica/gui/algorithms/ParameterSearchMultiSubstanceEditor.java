    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addButtonActionPerformed
        Object[] selectedNames = fixedList.getSelectedValues();
        for (int i = 0; i < selectedNames.length; i++) {
            if (!searchParameters.contains(selectedNames[i]))
                searchParameters.addElement(selectedNames[i]);
        }
    }// GEN-LAST:event_addButtonActionPerformed

    private void pauseButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_pauseButtonActionPerformed
        parameterSearch.pause();
    }// GEN-LAST:event_pauseButtonActionPerformed

    private void runButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_runButtonActionPerformed
        Parameter[] parameterList = new Parameter[searchParameters.size()];
        for (int i = 0; i < parameterList.length; i++) {
            parameterList[i] = system.getParameter((String) searchParameters
                    .get(i));
        }
        double time = Double.parseDouble(timeField.getText());
        double threshold = Double.parseDouble(thresholdField.getText());
        int maxIter = (int) Double.parseDouble(iterationsField.getText());
        parameterSearch = new ParameterSearchMultiSubstance(system,
                parameterList, targetList, threshold, time, maxIter);
        parameterSearch.start();
    }// GEN-LAST:event_runButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_removeButtonActionPerformed
        Object[] selectedNames = searchParameterList.getSelectedValues();
        for (int i = 0; i < selectedNames.length; i++) {
            searchParameters.removeElement(selectedNames[i]);
        }
    }// GEN-LAST:event_removeButtonActionPerformed

    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_resetButtonActionPerformed
        Object[] parameters = system.getParameters().toArray();
        for (int i = 0; i < parameters.length; i++) {
            Parameter p = (Parameter) parameters[i];
            p.resetSearchingValue();
        }
    }// GEN-LAST:event_resetButtonActionPerformed

    private void addSubstanceButtonActionPerformed(
            java.awt.event.ActionEvent evt) {// GEN-FIRST:event_addSubstanceButtonActionPerformed
        JFrame jf = new TargetSubstanceEditor(this);
        jf.pack();
        jf.show();
    }// GEN-LAST:event_addSubstanceButtonActionPerformed

    private void removeSubstanceButtonActionPerformed(
            java.awt.event.ActionEvent evt) {// GEN-FIRST:event_removeSubstanceButtonActionPerformed
        removeSelectedSubstance();
    }// GEN-LAST:event_removeSubstanceButtonActionPerformed

    private void editSubstanceButtonActionPerformed(
            java.awt.event.ActionEvent evt) {// GEN-FIRST:event_editSubstanceButtonActionPerformed
        String name = (String) targetListBox.getSelectedValue();
        TargetSubstance ts = null;
        for (TargetSubstance sub : targetList) {
            if (sub.getName().equals(name)) {
                ts = sub;
                break;
            }
        }
        removeSelectedSubstance();
        JFrame jf = new TargetSubstanceEditor(this, ts);
        jf.pack();
        jf.show();
    }// GEN-LAST:event_editSubstanceButtonActionPerformed

    public void removeSelectedSubstance() {
        Object selected = targetListBox.getSelectedValue();
        if (selected == null) {
            return;
        }
        String name = (String) selected;
        targetSubstances.removeElement(selected);
        for (TargetSubstance ts : targetList) {
            if (name.equals(ts.getName())) {
                targetList.remove(ts);
                break;
            }
        }
    }

