    public void setupLists() {
        List parameters = system.getParameters();
        for (int i = 0; i < parameters.size(); i++) {
            Parameter p = (Parameter) parameters.get(i);
            if (p.getName().equals("Time")) {
                continue;
            }
            fixedParameters.addElement(p.getName());
        }
    }

    public void addTargetSubstance(TargetSubstance ts) {
        targetList.add(ts);
        targetSubstances.addElement(ts.getName());
    }

